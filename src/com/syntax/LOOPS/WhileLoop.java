package com.syntax.LOOPS;

public class WhileLoop {

	public static void main(String[] args) {
		// print numbers from 1 to 10
		
		int num=1;
		while (num<=10) {
			System.out.print(num+" ");
			num++;
		}
		
		System.out.println("- - - - - --  -- ");
		int a=10;
		while (a<=25) {
			
			System.out.println(a);
			a++;
		}
		System.out.println("- - - - - --  -- ");
		int b=10;
		while (b>=1){
			System.out.print(b+" ");
			b--;			
		}
		System.out.println("- - - - - --  -- ");
		//print all num from 50 to 20
		// print all even numbers from 1 to 20
		int c=20;
	
		while(c<=20) {
			System.out.print(c+" ");
			c++;
		}
	}
	}
